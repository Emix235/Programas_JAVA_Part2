    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Validaci√≥n de Campo Vacio: Guardar
        cadena = jTextField1.getText();
        int cantidad = cadena.length();
        if(cantidad==0){
            //Campo esta Vacio
            JOptionPane.showMessageDialog(this, "Debe Llenar todos los campos", 
                "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Cantidad= "+cantidad, 
                "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
        
        // Validaci√≥n de Lista Desplegable
        if(jComboBox1.getSelectedIndex()==0){
            // Valor por Defecto
            JOptionPane.showMessageDialog(this, "Debe Seleccionar el Campo", 
                "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        }else{
            // Todos los valores excepto el Index = 0;
            cadena = ""+jComboBox1.getSelectedItem();
            JOptionPane.showMessageDialog(this, "Item = "+cadena, 
                "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField6KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField6KeyTyped
        // Validar la Longitud del Campo:
        cadena = jTextField6.getText();
        //if(cadena.length()<2 || cadena.length()>8){
        // And && OR  ||
        if(cadena.length()<2){
            // Longitud menor
            getToolkit().beep(); // Emite Sonido de Error (campana)
            jLabel10.setText("Error de Limite");
            jLabel10.setForeground(Color.red);
        }else if(cadena.length()>=8){
            // Longitud mayor
            getToolkit().beep(); // Emite Sonido de Error (campana)
            evt.consume(); // Borra el ultimo caracter
            jLabel10.setText("Error de Limite");
            jLabel10.setForeground(Color.red);
            //JOptionPane.showMessageDialog(this, "ERROR DE LIMITES", 
            //    "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
            jTextField6.setCursor(null); 
        }else{
            jLabel10.setText("Correcto");
            jLabel10.setForeground(Color.green);
        }
    }//GEN-LAST:event_jTextField6KeyTyped

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // Validar Mayusculas:
        char C= evt.getKeyChar();
        if(Character.isLowerCase(C)){
            String cad= (""+C).toUpperCase();
            C=cad.charAt(0);
            evt.setKeyChar(C);
        }
    }//GEN-LAST:event_jTextField2KeyTyped

    private void jTextField3KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyTyped
        // Validar no Entrada de Numeros:
        char C= evt.getKeyChar();
        if(Character.isDigit(C)){
            getToolkit().beep(); // Emite Sonido de Error (campana)
            evt.consume(); // Borra el ultimo caracter
            JOptionPane.showMessageDialog(this, 
                    "NO SE PERMITEN ENTRADA DE NUMEROS",
                    "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField3KeyTyped

