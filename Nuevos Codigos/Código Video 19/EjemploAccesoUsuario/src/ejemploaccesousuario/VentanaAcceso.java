    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Validar Entrada de Datos
            // Validar campos
            // Validar base de datos
        System.out.println("Validar campos");
        // Cerrar la Ventana Actual
        System.out.println("Cerrar Interfaz Actual");
        
        int resultado = JOptionPane.showConfirmDialog(null, "¿Desea Cerrar la Ventana?");
        
        switch (resultado) {
            case 0:
                //Opcion SI
                dispose();
                // Abrir siguiente interfaz
                //JOptionPane.showMessageDialog(null, "Bienvenido al Sistema");
                JOptionPane.showMessageDialog(null, "Bienvenido al Sistema", "Mensaje de Información",
                        JOptionPane.INFORMATION_MESSAGE);
                llamarInterfaz();
                break;
            case 1:
                //Opcion NO
                JOptionPane.showMessageDialog(null, "Entrada al Sistema Cancelado", "Mensaje de Error",
                        JOptionPane.ERROR_MESSAGE);
                break;
            default:
                break;
        }
        
        //Mensaje Emergente
        JOptionPane.showMessageDialog(null, "Bienvenido al Sistema", "Mensaje de Plano",
                JOptionPane.PLAIN_MESSAGE);
        JOptionPane.showMessageDialog(null, "Bienvenido al Sistema", "Mensaje de Pregunta",
                JOptionPane.QUESTION_MESSAGE);
        JOptionPane.showMessageDialog(null, "Bienvenido al Sistema", "Mensaje de Peligro",
                JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_jButton1ActionPerformed

